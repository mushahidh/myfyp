package bean;

public class top_user_cities {
	
		private String location;
		private double visits;
		private double visits_percentage;
		public String getLocation() {
			return location;
		}
		public void setLocation(String location) {
			this.location = location;
		}
		public double getVisits() {
			return visits;
		}
		public void setVisits(double visits) {
			this.visits = visits;
		}
		public double getVisits_percentage() {
			return visits_percentage;
		}
		public void setVisits_percentage(double visits_percentage) {
			this.visits_percentage = visits_percentage;
		}
}
