package bean;

public class top_user {
	private int user_id;
	private double user_clicks;
	private double total_percentage;
	private String user_name;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public double getUser_clicks() {
		return user_clicks;
	}
	public void setUser_clicks(double user_clicks) {
		this.user_clicks = user_clicks;
	}
	public double getTotal_percentage() {
		return total_percentage;
	}
	public void setTotal_percentage(double total_percentage) {
		this.total_percentage = total_percentage;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

}
