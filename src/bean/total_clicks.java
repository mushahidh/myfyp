package bean;

public class total_clicks {
	private double total_clicks;
	private double total_impressions;
	public double getTotal_clicks() {
		return total_clicks;
	}
	public void setTotal_clicks(double total_clicks) {
		this.total_clicks = total_clicks;
	}
	public double getTotal_impressions() {
		return total_impressions;
	}
	public void setTotal_impressions(double total_impressions) {
		this.total_impressions = total_impressions;
	}

}
